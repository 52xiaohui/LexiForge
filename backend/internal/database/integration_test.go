type fakeArticleAI struct{}

func (fakeArticleAI) GenerateArticle(_ context.Context, req ai.GenerateArticleRequest) (*ai.GenerateArticleResponse, error) {
	parts := make([]string, 0, len(req.TargetWords))
	claims := make([]ai.CoveredWord, 0, len(req.TargetWords))
	for i, target := range req.TargetWords {
		parts = append(parts, target.Word)
		after := ""
		if i < len(req.TargetWords)-1 {
			after = " "
		}
		claims = append(claims, ai.CoveredWord{
			Spelling:     target.Word,
			Form:         target.Word,
			Occurrence:   1,
			ContextAfter: after,
		})
	}
	return &ai.GenerateArticleResponse{
		Title:           "Integration Article",
		ContentMarkdown: strings.Join(parts, " "),
		Summary:         "Generated by a test fake.",
		CoveredWords:    claims,
		MissingWords:    []string{},
		ModelName:       "fake-integration-model",
		InputTokens:     11,
		OutputTokens:    22,
	}, nil
}

